
using System;

public enum SpheroRawMotorMode
{
    Off       = 0, /*!< Mode to set the motor off */
    Forward   = 1, /*!< Mode to set the motor to turn forward. */
    Reverse   = 2, /*!< Mode to set the motor to turn backward. */
    Brake     = 3, /*!< Mode to brake the motors. */
    Ignore    = 4  /*!< Mode to ignore the motor power value. */
};

